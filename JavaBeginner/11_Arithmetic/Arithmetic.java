public class Arithmetic {
    public static void main(String[] args) {
        int a = 10;
        int b = 13;

        System.out.println("Sum = " + (a + b));
        System.out.println("Total = " + (a - b));
        System.out.println("Total = " + (a * b));
        System.out.println("Total = " + (a / b));
        System.out.println("Mod = " + (a % b));
    }
}
